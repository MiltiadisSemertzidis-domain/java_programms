// This program demonstrates integer variables and basic arithmetic
class IntegerExample {

    public static void main(String[] args) {

        // Declare integer variable x
        int x;

        // Declare and initialize integer variable y
        int y = 3;

        // Assign value to x
        x = 10;

        // Declare integer variable z
        int z;

        // Calculate the value of z
        z = x + y + 7;

        // Print the results
        System.out.println(
            "\nThe integer z has the value of x + y + 7 = " + z +
            "\n x = " + x +
            "\n y = " + y
        );
    }
}
